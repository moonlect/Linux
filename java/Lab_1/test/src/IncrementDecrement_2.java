public class IncrementDecrement_2 {
    public static void main(String args[]) {
        int x = 2, y = 3;
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("++x = " + ++x);
        System.out.println("y++ = " + y++);
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("--x = " + --x);
        System.out.println("y-- = " + y--);
        System.out.println("x = " + x);
        System.out.println("y = " + y);


    }
}
